#import "CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *)darkRedColor
{
    return [UIColor colorWithRed:.38 green:.05 blue:.05 alpha:1];
}

+ (UIColor *)graniteColor
{
    return [UIColor colorWithRed:.15 green:.15 blue:.15 alpha:1];
}

+ (UIColor *)salmonColor
{
    return [UIColor colorWithRed:53.0/255 green:164.0/255 blue:237.0/255 alpha:1];
}

@end
